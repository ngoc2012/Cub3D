#include <stdlib.h>
#include "libft.h"

char	*ft_strndup(char *s, int len)
{
	char	*o;

	o = malloc(len + 1);
	if (!o)
		return (0);
	ft_memcpy(o, s, len);
	o[len] = 0;
	return (o);
}
